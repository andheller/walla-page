import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const sourcePath = path.join(repoRoot, "assets/audio/campfire-demo-source.mp3");
const outputPath = path.join(repoRoot, "src/lib/campfire-audio.ts");

const base64 = await readFile(sourcePath, { encoding: "base64" });

const output = `// Generated by scripts/generate-campfire-audio.mjs from assets/audio/campfire-demo-source.mp3
export const CAMPFIRE_DEMO_MP3_BASE64 = ${JSON.stringify(base64)};
`;

await writeFile(outputPath, output);
